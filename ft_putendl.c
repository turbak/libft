#include "libft.h"

void	ft_putendl(char const *s)
{
	size_t i;

	if (s != NULL)
	{
		i = 0;
		while (s[i])
			i++;
		if (i > 0)
		{
			write(1, s, i);
			write(1, "\n", 1);
		}
	}
}
